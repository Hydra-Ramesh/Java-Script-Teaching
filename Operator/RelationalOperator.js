let a = 30;
let b = 20;
let c = '30';

// Equal
let result = a==c;
console.log(result);

// Strict Equal
let result1 = a===c;
console.log(result1);

// Not Equal
let result2 = a!=c;
console.log(result2);

// Strict Not Equal
let result3 = a!==c;
console.log(result3);

// Greater Than
let result4 = a>b;
console.log(result4);

// Greater Than or Equal
let result5 = a>=b;
console.log(result5);

// Less Than
let result6 = a<b;
console.log(result6);

// Less Than or Equal
let result7 = a<=b;
console.log(result7);


