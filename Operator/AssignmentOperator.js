let a = 20;
let b = 10;

a = a + b; // Normal Operation
a+=b;//Short Hand Operation
console.log(a); // 30

a-= b; // Normal Operation
console.log(a); // 20
a*= b; // Short Hand Operation
console.log(a); // 200
a/= b; // Normal Operation
console.log(a); // 20
a%= b; // Short Hand Operation
console.log(a); // 0
a**= b; // Normal Operation
console.log(a); // 1